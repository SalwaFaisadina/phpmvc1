    <font color="black"> Biografi Mark Lee </font>
</h4>
<p>
    <font color="black"> &nbsp; &nbsp; &nbsp; &nbsp; Mark Lee lahir 2 Agustus 1999 di Toronto, Ontario, Kanada. Mark lahir dari keluarga blasteran Amerika dan Korea, Mark adalah anak kedua dari dua bersaudara, dia memiliki kakak laki-laki bernama Lee Jin Hyung. Mark Lee secara profesional dikenal sebagai Mark, adalah seorang rapper Kanada, penyanyi, penari dan penulis lagu yang tinggal di Korea Selatan. Saat ini Mark Lee berumur 23 tahun, tinggi Mark Lee adalah 175 cm. Dia adalah anggota boy band Korea Selatan NCT dan sub-unit tetapnya NCT 127 dan NCT DREAM, serta supergrup SuperM Korea Selatan. Selain itu ia juga menjadi leader di grup NCT DREAM, dan sebagai yang tertua di grup tersebut. <br> <br> &nbsp; &nbsp; &nbsp; &nbsp; Mark Lee tinggal di Toronto, Ontario, Kanada sampai berusia 7 tahun. Ia kemudian pindah ke New York, Amerika Serikat dan menetap di sana selama 5 tahun. Selama di Amerika Serikat, Mark bersekolah di P.S. 209 Clearview Gardens di Whitestone, Kota New York. Ia lalu kembali ke Kanada dan tinggal di Vancouver selama 2-3 tahun sampai akhirnya pada tahun 2012 ia pindah ke Seoul, Korea Selatan untuk bergabung dengan SM Entertainment. Mark melanjutkan pendidikannya di Eonju Middle School di Seoul dan lulus pada Februari 2015. Ia kemudian bersekolah di School of Performing Arts Seoul dan lulus pada tahun 2018. </font>
